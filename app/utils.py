def multiply(a: int | float, b: int | float) -> int | float:
    """
    Возвращает произведение двух чисел.

    Args:
        a: Первое число (целое или с плавающей точкой).
        b: Второе число (целое или с плавающей точкой).

    Returns:
        Произведение двух чисел.
    """
    return a * b
